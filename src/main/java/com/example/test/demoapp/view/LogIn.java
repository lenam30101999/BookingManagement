    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        controller = new UserController();
        if(jTextFieldUsername.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng đăng nhập tài khoản");
        } else if(jPasswordField1.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng đăng nhập mật khẩu");
        }else if(controller.connect().findPassword(jTextFieldUsername.getText().trim()).equals(
                    jPasswordField1.getText().trim()) == true){
                MenuForm menuForm = new MenuForm();
                menuForm.setVisible(true);
                this.dispose();
        }else{
                JOptionPane.showConfirmDialog(this, "Tài khoản không tồn tại");
        }
        System.out.println(jPasswordField1.getText()+" "+jTextFieldUsername.getText());
    }//GEN-LAST:event_jButton1ActionPerformed

